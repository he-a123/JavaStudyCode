package day05;

public abstract class Employee {
    private String ID;
    private String name;

    public  abstract void Work();

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}